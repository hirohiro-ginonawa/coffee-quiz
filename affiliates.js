// ★ 検索URL方式 ★
//   個別ASINに依存せず、Amazon検索結果ページに飛ばす方式。
//   → 商品が廃盤になっても壊れない／実在しないASINで404にならない。
//
// Amazonアソシエイト登録後の使い方：
//   1) 下の AFFILIATE_ID.amazon に自分のタグを入れる (例: 'coffeequiz-22')
//   2) それだけ。全リンクに自動で ?tag= が付与されます。
// ========================================================================

const AFFILIATE_ID = {
  amazon: 'YOUR_AMAZON_TAG-22',    // ← Amazonアソシエイト本登録後に置き換え (例: 'coffeequiz-22')
  rakuten: 'YOUR_RAKUTEN_ID',
  moshimo: 'YOUR_MOSHIMO_ID',
};

// Amazon 検索URL生成ヘルパー
// keyword: 検索キーワード / label: 表示ラベル
function amz(keyword, label) {
  const tag = AFFILIATE_ID.amazon;
  const isReal = tag && !tag.startsWith('YOUR_');
  const tagParam = isReal ? `&tag=${tag}` : '';
  return {
    url: `https://www.amazon.co.jp/s?k=${encodeURIComponent(keyword)}${tagParam}`,
    label: label
  };
}

