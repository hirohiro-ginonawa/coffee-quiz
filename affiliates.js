// ゲームからの利用関数
function renderAffiliates(level, container) {
  if (!container) return;
  const items = LEVEL_AFFILIATES[level] || [];
  container.innerHTML = '';
  items.forEach(item => {
    const a = document.createElement('a');
    a.className = 'affiliate-item';
    a.href = item.url;
    a.target = '_blank';
    a.rel = 'noopener nofollow sponsored';
    a.textContent = '☕ ' + item.label;
    container.appendChild(a);
  });
}
